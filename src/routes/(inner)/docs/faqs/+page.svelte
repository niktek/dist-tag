	<section class="space-y-4">
		<h2>Is Typescript required?</h2>
		<p>
			No, Skeleton does not require Typescript, though we highly recommend it. Please be aware all documentation examples are written with
			Typescript by default. If you wish to learn Typescript we recommend <a
				href="https://www.totaltypescript.com/tutorials"
				target="_blank"
				rel="noreferrer">Total Typescript</a
			>.
		</p>
	</section>

	<section class="space-y-4">
		<h2>What package managers are supported?</h2>
		<p>
			We support <a href="https://docs.npmjs.com/about-npm" target="_blank" rel="noreferrer">NPM (Node Package Manager)</a>. PNPM and Yarn
			are currently only supported for the Skeleton CLI.
		</p>
	</section>

	<section class="space-y-4">
		<h2>Can I use a monorepo?</h2>
		<p>
			Yes, but please be mindful of the <code>node_modules</code> path in your project configuration. We <u>do not</u> tailor instruction for
			this use case.
		</p>
	</section>

	<section class="space-y-4">
